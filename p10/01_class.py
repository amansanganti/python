class employee:
    Language = "Python"   # This is a class attributes
    salary = 24000000.   

Aman = employee()
Aman.name = "Aman Sanganti" # This is an instance attributes
print(Aman.name, Aman.Language, Aman.salary)

Rohan = employee()
Rohan.name = " Roronora Zoro"
print(Rohan.name, Rohan.Language, Rohan.salary)


# here name is instance attributes and salary and language are class attributes as they directly belong to the class



    